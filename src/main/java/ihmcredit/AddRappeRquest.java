package ihmcredit;

import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

public class AddRappeRquest {

	@DateTimeFormat(pattern = "yyyy-MM-dd")
	private Date	dateRappel;
	private String	libelle;
	private String	dateRappelAsStr;

	private String	detaille;

	private String	status	= "";

	private String	message	= "";

	private String	errotTile;

	public Date getDateRappel() {
		return dateRappel;
	}

	public void setDateRappel(final Date dateRappel) {
		this.dateRappel = dateRappel;
	}

	public String getLibelle() {
		return libelle;
	}

	public void setLibelle(final String libelle) {
		this.libelle = libelle;
	}

	public String getDetaille() {
		return detaille;
	}

	public void setDetaille(final String detaille) {
		this.detaille = detaille;
	}

	public String getDateRappelAsStr() {
		return dateRappelAsStr;
	}

	public void setDateRappelAsStr(final String dateRappelAsStr) {
		this.dateRappelAsStr = dateRappelAsStr;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(final String status) {
		this.status = status;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(final String message) {
		this.message = message;
	}

	public String getErrotTile() {
		return errotTile;
	}

	public void setErrotTile(final String errotTile) {
		this.errotTile = errotTile;
	}

}
