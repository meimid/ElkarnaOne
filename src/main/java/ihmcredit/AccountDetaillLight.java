package ihmcredit;

import java.io.Serializable;

import org.joda.time.LocalDate;

public class AccountDetaillLight implements Serializable {

	/**
	 * 
	 */
	private static final long	serialVersionUID	= -9033855143042062387L;

	/**
	 * 
	 */

	private Integer	          numAccountMovement;

	private String	          libelle;
	private String	          montantCredit;
	private String	          montantDebit;
	private boolean	          active	         = false;
	private String	          numCompte;
	private String	          accountLibelle;

	private LocalDate	      dateOperation;

	public LocalDate getDateOperation() {
		return dateOperation;
	}

	public void setDateOperation(final LocalDate date) {
		this.dateOperation = date;
	}

	public Integer getNumAccountMovement() {
		return numAccountMovement;
	}

	public void setNumAccountMovement(final Integer numAccountMovement) {
		this.numAccountMovement = numAccountMovement;
	}

	public String getLibelle() {
		return libelle;
	}

	public void setLibelle(final String libelle) {
		this.libelle = libelle;
	}

	/**
	 * @return the montantCredit
	 */
	public String getMontantCredit() {
		return montantCredit;
	}

	/**
	 * @param montantCredit
	 *            the montantCredit to set
	 */
	public void setMontantCredit(final String montantCredit) {
		this.montantCredit = montantCredit;
	}

	/**
	 * @return the montantDebit
	 */
	public String getMontantDebit() {
		return montantDebit;
	}

	/**
	 * @param montantDebit
	 *            the montantDebit to set
	 */
	public void setMontantDebit(final String montantDebit) {
		this.montantDebit = montantDebit;
	}

	/**
	 * @return the active
	 */
	public boolean isActive() {
		return active;
	}

	/**
	 * @param active
	 *            the active to set
	 */
	public void setActive(final boolean active) {
		this.active = active;
	}

	public String getNumCompte() {
		return numCompte;
	}

	public void setNumCompte(final String numCompte) {
		this.numCompte = numCompte;
	}

	public String getAccountLibelle() {
		return accountLibelle;
	}

	public void setAccountLibelle(final String accountLibelle) {
		this.accountLibelle = accountLibelle;
	}

}
